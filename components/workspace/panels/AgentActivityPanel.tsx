function getActivityIcon(activityId: string) {
  if (activityId.startsWith('opp-')) return <IconDatabase />
  if (activityId.startsWith('attachments-')) return <IconDocument />
  if (activityId.startsWith('brief-')) return <IconSparkles />
  if (activityId.startsWith('assessment-')) return <IconCalculator />
  if (activityId.startsWith('sow-')) return <IconDocument />
  if (activityId.startsWith('subs-')) return <IconUsers />
  return <IconSparkles />
}

// ─── Activity Row ─────────────────────────────────────────────────────────────

function ActivityRow({ activity, isLast }: { activity: AgentActivity; isLast: boolean }) {
  const statusDot = {
    completed: 'bg-stone-500',
    failed: 'bg-stone-400',
    pending: 'bg-stone-300',
  }[activity.status]

  const statusRing = {
    completed: 'ring-stone-200',
    failed: 'ring-stone-200',
    pending: 'ring-stone-100',
  }[activity.status]

  const statusLabel = {
    completed: 'Completed',
    failed: 'Partial failure',
    pending: 'Pending',
  }[activity.status]

  return (
    <div className="flex gap-3">
      {/* Timeline spine */}
      <div className="flex flex-col items-center flex-shrink-0">
        <div className={`w-6 h-6 rounded-full bg-white ring-2 ${statusRing} flex items-center justify-center text-stone-500 mt-0.5`}>
          {getActivityIcon(activity.id)}
        </div>
        {!isLast && <div className="w-px flex-1 bg-stone-100 mt-1 min-h-[1.5rem]" />}
      </div>

      {/* Content */}
      <div className={`pb-5 min-w-0 flex-1`}>
        <div className="flex items-start justify-between gap-2">
          <div className="min-w-0">
            <p className="text-sm font-medium text-stone-800 leading-snug">{activity.label}</p>
            <p className="text-xs text-stone-500 mt-0.5 leading-relaxed">{activity.description}</p>
            {activity.detail && (
              <p className="text-[11px] text-stone-400 mt-1 font-mono leading-snug">{activity.detail}</p>
            )}
          </div>
          <div className="flex-shrink-0 text-right">
            {activity.timestamp && (
              <p
                className="text-[11px] text-stone-400 whitespace-nowrap"
                title={format(activity.timestamp, 'PPpp')}
              >
                {formatDistanceToNow(activity.timestamp, { addSuffix: true })}
              </p>
            )}
          </div>
        </div>

        {/* Source badges */}
        <div className="flex items-center gap-1.5 mt-2 flex-wrap">
          <span className={`inline-flex items-center gap-1 px-1.5 py-0.5 rounded text-[10px] font-medium ${
            activity.status === 'completed'
              ? 'bg-stone-100 text-stone-600'
              : activity.status === 'failed'
              ? 'bg-stone-100 text-stone-500'
              : 'bg-stone-50 text-stone-400'
          }`}>
            <span className={`w-1.5 h-1.5 rounded-full ${statusDot}`} />
            {statusLabel}
          </span>
          <span className="inline-flex items-center px-1.5 py-0.5 rounded text-[10px] bg-stone-50 text-stone-500 border border-stone-100">
            {activity.model}
          </span>
        </div>
      </div>
    </div>
  )
}

// ─── Stats Bar ────────────────────────────────────────────────────────────────

function StatsBar({ activities }: { activities: AgentActivity[] }) {
  const total = activities.length
  const completed = activities.filter((a) => a.status === 'completed').length
  const aiActions = activities.filter(
    (a) => a.model.includes('GPT') || a.model.includes('OpenAI')
  ).length

  return (
    <div className="grid grid-cols-3 gap-3 mb-6">
      <StatTile label="AI Actions" value={String(aiActions)} />
      <StatTile label="Total Events" value={String(total)} />
      <StatTile label="Completed" value={`${completed}/${total}`} />
    </div>
  )
}

function StatTile({ label, value }: { label: string; value: string }) {
  return (
    <div className="bg-white border border-stone-200 rounded-lg p-3 text-center">
      <p className="text-xs font-bold text-stone-800">{value}</p>
      <p className="text-[10px] uppercase tracking-wider text-stone-400 mt-0.5">{label}</p>
    </div>
  )
}

// ─── Empty State ──────────────────────────────────────────────────────────────

function EmptyState() {
  return (
    <div className="flex flex-col items-center justify-center py-16 text-center">
      <div className="w-10 h-10 rounded-full bg-stone-100 flex items-center justify-center mb-3">
        <IconSparkles />
      </div>
      <p className="text-sm font-medium text-stone-700">No agent activity yet</p>
      <p className="text-xs text-stone-400 mt-1 max-w-xs">
        AI actions will appear here once you generate a SOW, discover subcontractors, or create a bid assessment.
      </p>
    </div>
  )
}

// ─── Panel ────────────────────────────────────────────────────────────────────

export default function AgentActivityPanel({
  opportunity,
  assessment,
}: AgentActivityPanelProps) {
  const activities = useMemo(
    () => deriveActivities(opportunity, assessment),
    [opportunity, assessment]
  )

  return (
    <div className="h-full overflow-y-auto bg-stone-50">
      <div className="max-w-2xl mx-auto px-6 py-6">
        {/* Header */}
        <div className="mb-6">
          <h2 className="text-base font-semibold text-stone-900">Agent Activity</h2>
          <p className="text-xs text-stone-500 mt-0.5">
            Chronological log of AI actions taken on this opportunity
          </p>
        </div>

        {activities.length === 0 ? (
          <EmptyState />
        ) : (
          <>
            <StatsBar activities={activities} />

            {/* Timeline */}
            <div>
              {activities.map((activity, idx) => (
                <ActivityRow
                  key={activity.id}
                  activity={activity}
                  isLast={idx === activities.length - 1}
                />
              ))}
            </div>

            {/* Footer note */}
            <div className="mt-4 pt-4 border-t border-stone-100">
              <p className="text-[10px] text-stone-400 leading-relaxed">
                Activity is derived from existing opportunity data (SOW timestamps, parsed attachment metadata, subcontractor discovery records). No separate activity log table is used.
              </p>
            </div>
          </>
        )}
      </div>
    </div>
  )
}
