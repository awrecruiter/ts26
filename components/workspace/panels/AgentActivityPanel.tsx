'use client'

import { useMemo } from 'react'
import { format, formatDistanceToNow } from 'date-fns'

// ─── Types ────────────────────────────────────────────────────────────────────

type ActivityStatus = 'completed' | 'failed' | 'pending'

interface AgentActivity {
  id: string
  label: string
  description: string
  timestamp: Date | null
  status: ActivityStatus
  model: string
  source: string
  detail?: string
}

interface AgentActivityPanelProps {
  opportunity: any
  assessment: any
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

function deriveActivities(opportunity: any, assessment: any): AgentActivity[] {
  const activities: AgentActivity[] = []

  // 1. Opportunity added to USHER (always present)
  if (opportunity?.createdAt) {
    activities.push({
      id: 'opp-added',
      label: 'Opportunity indexed',
      description: 'Solicitation data fetched from SAM.gov and stored',
      timestamp: new Date(opportunity.createdAt),
      status: 'completed',
      model: 'SAM.gov API',
      source: 'SAM.gov Opportunities API',
      detail: opportunity.solicitationNumber
        ? `Solicitation ${opportunity.solicitationNumber}`
        : undefined,
    })
  }

  // 2. Attachments parsed
  const parsedAttachments = opportunity?.parsedAttachments as any
  if (parsedAttachments?.parsedAt) {
    const parsedCount = parsedAttachments.parsedCount ?? parsedAttachments.parsed?.length ?? 0
    const totalCount = parsedAttachments.totalAttachments ?? 0
    const hasFailed = parsedCount < totalCount
    activities.push({
      id: 'attachments-parsed',
      label: 'Solicitation documents parsed',
      description: hasFailed
        ? `Parsed ${parsedCount} of ${totalCount} attachments — some could not be read`
        : `Extracted text and structured content from ${parsedCount} solicitation attachment${parsedCount !== 1 ? 's' : ''}`,
      timestamp: new Date(parsedAttachments.parsedAt),
      status: hasFailed ? 'failed' : 'completed',
      model: 'pdf-parse / mammoth',
      source: 'Attachment Parser',
      detail: totalCount > 0
        ? `${parsedCount}/${totalCount} documents · ${parsedAttachments.structured?.scope?.length ?? 0} scope items extracted`
        : undefined,
    })
  }

  // 3. Opportunity brief generated
  const brief = opportunity?.opportunityBrief as any
  if (brief?.generatedAt) {
    activities.push({
      id: 'brief-generated',
      label: 'Opportunity brief generated',
      description: 'Plain-language summary of what the agency is buying, who qualifies, and key gotchas',
      timestamp: new Date(brief.generatedAt),
      status: 'completed',
      model: 'GPT-4o',
      source: 'OpenAI',
      detail: brief.estimatedValue && brief.estimatedValue !== 'Not stated'
        ? `Estimated value: ${brief.estimatedValue}`
        : brief.contractType
          ? `Contract type: ${brief.contractType}`
          : undefined,
    })
  }

  // 4. Assessment created / updated
  if (assessment?.createdAt) {
    const isAutoGenerated =
      assessment.notes?.includes('USASpending') ||
      assessment.notes?.includes('usaspending') ||
      !assessment.notes
    activities.push({
      id: 'assessment-created',
      label: 'Bid assessment created',
      description: `Go/no-go analysis: ${assessment.recommendation ?? 'Pending'} · margin ${
        assessment.profitMarginPercent != null
          ? `${assessment.profitMarginPercent.toFixed(0)}%`
          : '—'
      }`,
      timestamp: new Date(assessment.createdAt),
      status: 'completed',
      model: isAutoGenerated ? 'Rule-based engine' : 'Manual',
      source: 'Margin Calculator',
      detail:
        assessment.estimatedValue != null
          ? `Value: $${formatCurrency(assessment.estimatedValue)} · Cost: $${formatCurrency(assessment.estimatedCost ?? 0)}`
          : undefined,
    })
  }

  // 5. Assessment updated (if updatedAt differs materially from createdAt)
  if (
    assessment?.updatedAt &&
    assessment?.createdAt &&
    Math.abs(
      new Date(assessment.updatedAt).getTime() - new Date(assessment.createdAt).getTime()
    ) > 60_000
  ) {
    activities.push({
      id: 'assessment-updated',
      label: 'Bid assessment updated',
      description: `Revised estimates — recommendation: ${assessment.recommendation ?? '—'}`,
      timestamp: new Date(assessment.updatedAt),
      status: 'completed',
      model: 'Manual',
      source: 'Margin Calculator',
    })
  }

  // 6. SOW(s) generated
  const sows: any[] = opportunity?.sows ?? []
  for (const sow of sows) {
    const sowContent = sow.content as any
    const usedOpenAI = sowContent?.sourceEnhanced !== false
    activities.push({
      id: `sow-generated-${sow.id}`,
      label: sow.version > 1 ? `SOW v${sow.version} generated` : 'Statement of Work generated',
      description: `${usedOpenAI ? 'GPT-4o drafted sections 1–6 from parsed solicitation content' : 'Rule-based sections built from solicitation metadata'}`,
      timestamp: new Date(sow.createdAt),
      status: 'completed',
      model: usedOpenAI ? 'GPT-4o' : 'Rule-based builder',
      source: usedOpenAI ? 'OpenAI' : 'USHER',
      detail: sow.fileName ?? undefined,
    })
  }

  // 7. Subcontractors discovered
  const subs: any[] = opportunity?.subcontractors ?? []
  if (subs.length > 0) {
    // Group by discovery batch (same-minute createdAt = same discovery run)
    const batches = new Map<string, any[]>()
    for (const sub of subs) {
      const bucketKey = sub.createdAt
        ? new Date(sub.createdAt).toISOString().slice(0, 16) // YYYY-MM-DDTHH:MM
        : 'unknown'
      if (!batches.has(bucketKey)) batches.set(bucketKey, [])
      batches.get(bucketKey)!.push(sub)
    }

    for (const [bucketKey, batch] of batches.entries()) {
      const googleCount = batch.filter((s) => s.source === 'google_places').length
      const samCount = batch.filter((s) => s.source === 'sam_gov').length
      const manualCount = batch.filter(
        (s) => s.source !== 'google_places' && s.source !== 'sam_gov'
      ).length

      const sourceLabel =
        googleCount > 0 && samCount > 0
          ? 'Google Places + SAM.gov'
          : googleCount > 0
          ? 'Google Places'
          : samCount > 0
          ? 'SAM.gov'
          : 'Manual'

      const parts: string[] = []
      if (googleCount > 0) parts.push(`${googleCount} via Google Maps`)
      if (samCount > 0) parts.push(`${samCount} via SAM.gov`)
      if (manualCount > 0) parts.push(`${manualCount} added manually`)

      const earliest = batch.reduce((min, s) => {
        const d = new Date(s.createdAt)
        return d < min ? d : min
      }, new Date(batch[0].createdAt))

      activities.push({
        id: `subs-discovered-${bucketKey}`,
        label: `${batch.length} subcontractor${batch.length !== 1 ? 's' : ''} discovered`,
        description: parts.join(', ') || `${batch.length} vendors found`,
        timestamp: earliest,
        status: 'completed',
        model: sourceLabel,
        source: sourceLabel,
        detail:
          googleCount > 0 && opportunity.naicsCode
            ? `NAICS ${opportunity.naicsCode} · ${opportunity.state ?? ''}`
            : undefined,
      })
    }
  }

  // Sort chronologically (oldest first)
  return activities.sort((a, b) => {
    if (!a.timestamp) return 1
    if (!b.timestamp) return -1
    return a.timestamp.getTime() - b.timestamp.getTime()
  })
}

function formatCurrency(amount: number): string {
  if (amount >= 1_000_000) return `${(amount / 1_000_000).toFixed(1)}M`
  if (amount >= 1_000) return `${(amount / 1_000).toFixed(0)}K`
  return amount.toLocaleString()
}

